import { useState, useEffect, useRef } from 'react'
import { erData } from './data.js'

const TABS = ['Diagram', 'Entities', 'Relationships']

export default function App() {
  const [activeTab, setActiveTab] = useState('Diagram')
  const [selectedEntity, setSelectedEntity] = useState(null)
  const [mermaidReady, setMermaidReady] = useState(false)
  const [copied, setCopied] = useState(false)
  const [showRaw, setShowRaw] = useState(false)
  const diagramRef = useRef(null)

  useEffect(() => {
    const script = document.createElement('script')
    script.type = 'module'
    script.textContent = `
      import mermaid from 'https://cdn.jsdelivr.net/npm/mermaid@10/dist/mermaid.esm.min.mjs';
      window.__mermaid = mermaid;
      mermaid.initialize({ startOnLoad: false, theme: 'dark', themeVariables: {
        primaryColor: '#7c6aff', primaryTextColor: '#e0e0e0', primaryBorderColor: '#7c6aff',
        lineColor: '#555', secondaryColor: '#1a1a2e', tertiaryColor: '#0d0d1a'
      }});
      window.dispatchEvent(new Event('mermaid-ready'));
    `
    document.head.appendChild(script)
    const handler = () => setMermaidReady(true)
    window.addEventListener('mermaid-ready', handler)
    return () => window.removeEventListener('mermaid-ready', handler)
  }, [])

  useEffect(() => {
    if (!mermaidReady || activeTab !== 'Diagram' || !diagramRef.current) return
    const render = async () => {
      try {
        diagramRef.current.innerHTML = ''
        const { svg } = await window.__mermaid.render('er-diagram', erData.mermaidDiagram)
        diagramRef.current.innerHTML = svg
      } catch (e) {
        diagramRef.current.innerHTML = `<pre style="color:#f87171">Mermaid render error: ${e.message}</pre>`
      }
    }
    render()
  }, [mermaidReady, activeTab])

  const copyDiagram = () => {
    navigator.clipboard.writeText(erData.mermaidDiagram)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }

  const uniqueSources = [...new Set(erData.entities.map(e => e.sourceFile))]

  return (
    <div className="app">
      {/* Sidebar */}
      <aside className="sidebar">
        <div className="sidebar-header">
          <span className="logo">🗄️</span>
          <h2>Entities</h2>
        </div>
        <div className="entity-list">
          {erData.entities.map(e => (
            <div
              key={e.name}
              className={`entity-item ${selectedEntity === e.name ? 'selected' : ''}`}
              onClick={() => { setSelectedEntity(e.name); setActiveTab('Entities') }}
            >
              <span className="entity-dot" />
              {e.name}
            </div>
          ))}
        </div>
      </aside>

      {/* Main */}
      <main className="main">
        {/* Header */}
        <header className="header">
          <div>
            <h1>ER Diagram — {erData.repoName}</h1>
            <p className="subtitle">Generated by polyglot-eval I1 • {new Date(erData.generatedAt).toLocaleString()}</p>
          </div>
          <div className="badges">
            <span className="badge badge-purple">{erData.entities.length} Entities</span>
            <span className="badge badge-teal">{erData.relationships.length} Relationships</span>
            <span className="badge badge-blue">{uniqueSources.length} Source Files</span>
          </div>
        </header>

        {/* Tabs */}
        <div className="tab-bar">
          {TABS.map(t => (
            <button
              key={t}
              className={`tab ${activeTab === t ? 'active' : ''}`}
              onClick={() => setActiveTab(t)}
            >{t}</button>
          ))}
        </div>

        {/* Content */}
        <div className="content">
          {activeTab === 'Diagram' && (
            <div className="diagram-tab">
              <div className="diagram-container" ref={diagramRef}>
                {!mermaidReady && <p className="loading">Loading Mermaid diagram engine...</p>}
              </div>
              <div className="raw-toggle">
                <button className="btn-secondary" onClick={() => setShowRaw(!showRaw)}>
                  {showRaw ? '▲ Hide' : '▼ Show'} Raw Mermaid Source
                </button>
                <button className="btn-copy" onClick={copyDiagram}>
                  {copied ? '✓ Copied!' : '📋 Copy'}
                </button>
              </div>
              {showRaw && (
                <pre className="raw-source">{erData.mermaidDiagram}</pre>
              )}
            </div>
          )}

          {activeTab === 'Entities' && (
            <div className="entities-grid">
              {erData.entities.map(entity => (
                <div
                  key={entity.name}
                  className={`entity-card ${selectedEntity === entity.name ? 'highlighted' : ''}`}
                  id={`entity-${entity.name}`}
                >
                  <div className="entity-card-header">
                    <h3>{entity.name}</h3>
                    <span className="source-badge">
                      {entity.sourceFile}:{entity.sourceLine}
                    </span>
                  </div>
                  <table className="column-table">
                    <thead>
                      <tr><th>Column</th><th>Type</th><th>Keys</th><th>References</th></tr>
                    </thead>
                    <tbody>
                      {entity.columns.map(col => (
                        <tr key={col.name}>
                          <td className="col-name">{col.name}</td>
                          <td className="col-type">{col.type}</td>
                          <td>
                            {col.isPK && <span className="key-badge pk">PK</span>}
                            {col.isFK && <span className="key-badge fk">FK</span>}
                          </td>
                          <td className="col-ref">{col.references || '—'}</td>
                        </tr>
                      ))}
                    </tbody>
                  </table>
                </div>
              ))}
            </div>
          )}

          {activeTab === 'Relationships' && (
            <div className="relationships-tab">
              {erData.relationships.length === 0 ? (
                <div className="empty-state">
                  <p>No relationships found. The entities may be independent or relationships are inferred from naming conventions.</p>
                </div>
              ) : (
                <table className="rel-table">
                  <thead>
                    <tr>
                      <th>From</th><th>To</th><th>Type</th><th>Label</th><th>Source</th>
                    </tr>
                  </thead>
                  <tbody>
                    {erData.relationships.map((r, i) => (
                      <tr key={i} className={r.type === 'inferred' ? 'inferred' : ''}>
                        <td className="rel-entity">{r.from}</td>
                        <td className="rel-entity">{r.to}</td>
                        <td>
                          <span className={`type-badge ${r.type}`}>{r.type}</span>
                        </td>
                        <td>{r.label}</td>
                        <td className="source-cite">{r.sourceFile}:{r.sourceLine}</td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              )}
            </div>
          )}
        </div>
      </main>
    </div>
  )
}
